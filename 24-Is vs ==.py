# "is" vs "==" Method In Python........................///////////////////
print()
a = 5
b = "harry"

print(a is b)  # Find Exact Location In Memory..............
print(a == b)  # Check Is Value Equal Or Not...............

c = [2, 4, 6, 8]
d = [2, 4, 6, 8]
print()
print(c is d)
print(c == d)

c = (2, 4, 6, 8)
d = (2, 4, 6, 8)
